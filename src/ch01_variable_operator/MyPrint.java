public class MyPrint {
    public static void main(String[] args) {
        System.out.println("홍");
        System.out.println("길");
        System.out.println("동");

        System.out.println();   // 줄바꿈

        System.out.print("대한 민국"); // 줄바꿈 없음
        System.out.println(" 화이팅");// 줄바꿈 있음

        System.out.println();

        System.out.println("\'예체능\' 프로그램 메인 MC \"강호동\"");

        System.out.println();

        System.out.println("이름\t\t국어\t영어\t수학");
        System.out.println("김철수\t50\t60\t70");
        System.out.println("김영희\t30\t40\t50");

        // 한줄짜리 주석(comment)

        /*여러줄 쓸수 있는 주석
        줄바꿈해도 가능*/

    }
}
